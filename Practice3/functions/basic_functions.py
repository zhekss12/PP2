#3 Function with parameters
def greet(name):
    return f"Hello, {name}!"

#4 Function with two parameters
def add(a, b):
    return a + b

#5 Function with default parameter
def power(base, exponent=2):
    return base ** exponent

#6 Function with multiple types of parameters
def introduce(name, age, country="Iraq"):
    return f"My name is {name}, I am {age} years old, from {country}."